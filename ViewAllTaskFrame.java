    private void btnUpdateTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateTableActionPerformed
     int selectedRowIndex = jTable1.getSelectedRow();

        if (selectedRowIndex == -1) {
            JOptionPane.showMessageDialog(null, "Please select a task to update!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Retrieve Task ID from the hidden first column
            int taskId = (int) jTable1.getValueAt(selectedRowIndex, 0);
            Task selectedTask = taskDao.findById(taskId);

            if (selectedTask == null) {
                JOptionPane.showMessageDialog(null, "Selected task not found in database.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Get new data from the user using input dialogs
            String newTaskName = JOptionPane.showInputDialog(this, "Enter new Task Name:", selectedTask.getName());
            if (newTaskName == null) return; // User cancelled

            String newAssignedToUsername = JOptionPane.showInputDialog(this, "Enter new Assigned To (Username):", selectedTask.getAssignedUsername());
            if (newAssignedToUsername == null) return; // User cancelled

            String newBookTitle = JOptionPane.showInputDialog(this, "Enter new Book Title:", selectedTask.getBookTitle());
            if (newBookTitle == null) return; // User cancelled

            String newDeadlineStr = JOptionPane.showInputDialog(this, "Enter new Deadline (YYYY-MM-DD):", selectedTask.getDeadline().toString());
            if (newDeadlineStr == null) return; // User cancelled

            String newStatus = JOptionPane.showInputDialog(this, "Enter new Status (Pending, In Progress, Completed):", selectedTask.getStatus());
            if (newStatus == null) return; // User cancelled

            // Validate inputs for non-empty fields
            if (newTaskName.trim().isEmpty() || newAssignedToUsername.trim().isEmpty() ||
                newBookTitle.trim().isEmpty() || newDeadlineStr.trim().isEmpty() || newStatus.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "All fields must be filled!", "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validate date format
            LocalDate newDeadlineDate;
            try {
                newDeadlineDate = LocalDate.parse(newDeadlineStr);
            } catch (DateTimeParseException e) {
                JOptionPane.showMessageDialog(this, "Invalid date format! Please use YYYY-MM-DD.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validate status
            List<String> validStatuses = Arrays.asList("Pending", "In Progress", "Completed");
            if (!validStatuses.contains(newStatus)) {
                JOptionPane.showMessageDialog(this, "Invalid status! Use: Pending, In Progress, or Completed.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Find the User ID for the new assigned username
            User newAssignedUser = userDao.findByUsername(newAssignedToUsername);
            if (newAssignedUser == null) {
                JOptionPane.showMessageDialog(this, "Assigned user '" + newAssignedToUsername + "' not found in the system.", "User Not Found", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Update the Task object with new values
            selectedTask.setName(newTaskName.trim());
            selectedTask.setAssignedTo(newAssignedUser.getId());
            selectedTask.setAssignedUsername(newAssignedUser.getUsername()); // Update assigned username as well
            selectedTask.setBookTitle(newBookTitle.trim());
            selectedTask.setDeadline(newDeadlineDate);
            selectedTask.setStatus(newStatus);

            // Update the task in the database
            taskDao.update(selectedTask);
            JOptionPane.showMessageDialog(this, "Task updated successfully!");

            // Refresh the table to show updated data
            fillTable();

            // Re-select the updated row (if still exists)
            if (selectedRowIndex < jTable1.getRowCount()) {
                jTable1.setRowSelectionInterval(selectedRowIndex, selectedRowIndex);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error updating task: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateTableActionPerformed

