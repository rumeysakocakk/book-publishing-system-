    /**
     * Fills the jTable1 with all tasks from the database.
     * Includes Task ID as a hidden first column for internal use.
     */
    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        // Fetch all tasks from the database using TaskDao
        List<Task> allTasks = taskDao.findAll();

        for (Task task : allTasks) {
            // Ensure the AssignedTo user exists or display "N/A"
            String assignedUsername = task.getAssignedUsername() != null ? task.getAssignedUsername() : "N/A";
            
            // Add task data to the table: ID (hidden), Task Name, Assigned To, Book Title, Deadline, Status
            model.addRow(new Object[]{ // Hidden ID
                task.getName(),
                task.getAssignedTo(),
                task.getBookTitle(),
                task.getDeadline() != null ? task.getDeadline().toString() : "N/A", // Convert LocalDate to String
                task.getStatus()
            });
        }
    }

