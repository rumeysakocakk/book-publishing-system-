    private void txtSearchFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSearchFieldActionPerformed
        String searchText = txtSearchField.getText().toLowerCase().trim();
        List<Task> filteredTasks = new java.util.ArrayList<>();

        List<Task> allTasks = taskDao.findAll(); // Fetch all tasks from DB

        for (Task task : allTasks) {
            // Check if any of the task's fields contain the search text (case-insensitive)
            if (task.getName().toLowerCase().contains(searchText) ||
                (task.getAssignedUsername() != null && task.getAssignedUsername().toLowerCase().contains(searchText)) ||
                (task.getBookTitle() != null && task.getBookTitle().toLowerCase().contains(searchText)) ||
                (task.getDeadline() != null && task.getDeadline().toString().toLowerCase().contains(searchText)) ||
                (task.getStatus() != null && task.getStatus().toLowerCase().contains(searchText))) {
                filteredTasks.add(task);
            }
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Task task : filteredTasks) {
            // Add task data to the table: ID (hidden), Task Name, Assigned To, Book Title, Deadline, Status
            model.addRow(new Object[]{
                task.getId(), // Hidden ID
                task.getName(),
                task.getAssignedUsername(),
                task.getBookTitle(),
                task.getDeadline() != null ? task.getDeadline().toString() : "N/A",
                task.getStatus()
            });
        }

    }//GEN-LAST:event_txtSearchFieldActionPerformed

    private void btnQueryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQueryActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow != -1) {
            try {
                // Retrieve Task ID from the hidden first column
                int taskId = (int) jTable1.getValueAt(selectedRow, 0);
                Task task = taskDao.findById(taskId); // Fetch task from database

                if (task != null) {
                    String message = String.format(
                        "Task ID: %d\nTask Name: %s\nAssigned To: %s\nBook Title: %s\nDeadline: %s\nStatus: %s\nComment: %s",
                        task.getId(),
                        task.getName(),
                        task.getAssignedUsername(),
                        task.getBookTitle(),
                        task.getDeadline() != null ? task.getDeadline().toString() : "N/A",
                        task.getStatus(),
                        task.getComment() != null ? task.getComment() : "No comment."
                    );
                    JOptionPane.showMessageDialog(this, message, "Task Details", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Selected task not found in the system (database error).", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error retrieving task details: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a task to query.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnQueryActionPerformed

